function digitsWithWords(numString) {
	switch (numString) {
		case 'nine':
			console.log(9);

			break;
		case 'eight':
			console.log(8);

			break;
		case 'seven':
			console.log(7);

			break;
		case 'six':
			console.log(6);

			break;
		case 'five':
			console.log(5);

			break;
		case 'four':
			console.log(4);

			break;
		case 'three':
			console.log(3);

			break;

		case 'two':
			console.log(2);

			break;
		case 'one':
			console.log(1);

			break;
		case 'zero':
			console.log(0);

			break;

		default:
			break;
	}
}
digitsWithWords('nine');
