function calculator(firstNum, secondNum, desider) {
	if (desider === 'multiply') {
		console.log(firstNum * secondNum);
	} else if (desider === 'divide') {
		console.log(firstNum / secondNum);
	} else if (desider === 'add') {
		console.log(firstNum + secondNum);
	} else if (desider === 'subtract') {
		console.log(firstNum - secondNum);
	}
}
